def generate_words_json():
    """Generate words.json file from AWL data"""
    words = []
    word_id = 1
    
    for level, word_list in AWL_WORDS.items():
        for word_data in word_list:
            word, pos, definition, example, category = word_data
            words.append({
                "id": word_id,
                "word": word,
                "level": level,
                "partOfSpeech": pos,
                "definition": definition,
                "example": example,
                "category": category
            })
            word_id += 1
    
    return words

def main():
    words = generate_words_json()
    
    # Save to assets/data/words.json
    output_path = os.path.join(os.path.dirname(__file__), 'assets', 'data', 'words.json')
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(words, f, indent=2, ensure_ascii=False)
    
    # Print statistics
    print(f"‚úÖ Generated {len(words)} words")
    print("\nDistribution by Band level:")
    for level in ["Band 5", "Band 6", "Band 7", "Band 8+"]:
        count = sum(1 for w in words if w["level"] == level)
        print(f"  {level}: {count} words")
    
    print("\nDistribution by Category:")
    categories = {}
    for w in words:
        cat = w["category"]
        categories[cat] = categories.get(cat, 0) + 1
    for cat, count in sorted(categories.items(), key=lambda x: -x[1]):
        print(f"  {cat}: {count} words")
    
    print(f"\nüìÅ Saved to: {output_path}")

if __name__ == "__main__":
    main()
